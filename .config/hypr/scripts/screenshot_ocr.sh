#!/bin/bash

# 1. Выбрать область экрана
geometry=$(slurp)

# 2. Если область не выбрана, выйти
if [ -z "$geometry" ]; then
  exit 0
fi

# 3. Сделать скриншот области, распознать текст (рус+англ) и скопировать в буфер обмена
grim -g "$geometry" - | tesseract -l rus+eng stdin stdout | wl-copy

# 4. Отправить уведомление
notify-send "Текст скопирован" "Текст из выбранной области скопирован в буфер обмена."
